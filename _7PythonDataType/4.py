x = 1    # int
y = 2.8  # float
z = 1j   # complex

#convert from int to float:
a = float(x)

#convert from float to int:
b = int(y)

#convert from int to complex:
c = complex(z)

print(a)
print(b)
print(c)

print(type(a)) # <class float>
print(type(b)) # <class int>
print(type(c)) # <class complex>