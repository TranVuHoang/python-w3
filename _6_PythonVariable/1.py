# Ép kiểu cho biến:
x = str(3) # x will be '3'(kiểu chuỗi)
y = int(3) # y will be 3 (kiểu số nguyên)
z = float(3) # z will be 3.0(kiểu float)

print(x) #3
print(y) #3
print(z) #3.0

